// To recap, push() pushes elements onto the ends of arrays, and pop() pops them off; similarly, unshift() adds elements to the beginnings of arrays, and shift() pulls them off. Now it's time to practice what we've learned.



const append = ["Milo", "Otis", "Garfield"]
append.push("Odie")

const prepend = ["Milo", "Otis", "Garfield"]
prepend.unshift("Odie")

const removeLast = ["Milo", "Otis", "Garfield"]
removeLast.pop()

const removeFirst = ["Milo", "Otis", "Garfield"]
removeFirst.shift()
